// Definitions related days
// (i) EC_DAY_OF_WEEK. You need to provide a data type called EC_DAY_OF_WEEK
// There are seven allowed values:
// EC_MONDAY, with value equal to 1
// EC_TUESDAY, with value equal to 2
// EC_WEDNESDAY, with value equal to 3,
// ...
// EC_SUNDAY, with value equal to 7

#ifndef EC_DAY_OF_WEEK_H
#define EC_DAY_OF_WEEK_H

enum EC_DAY_OF_WEEK {
    EC_MONDAY = 1,
    EC_TUESDAY = 2,
    EC_WEDNESDAY = 3,
    EC_THURSDAY = 4,
    EC_FRIDAY = 5,
    EC_SATURDAY = 6,
    EC_SUNDAY = 7,
};

#endif